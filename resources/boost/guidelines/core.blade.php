## Laravel Mixins

- `protonemedia/laravel-mixins` provides opt-in Blade directives, validation rules, string macros, and request utilities for Laravel applications.
- Always activate the `laravel-mixins-development` skill when working with Blade directives, validation rules, string macros, PDF regeneration, base64 file conversion, or any code that uses `ProtoneMedia\LaravelMixins` classes.
